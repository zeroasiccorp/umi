               // outgoing UMI
               .umi_out_valid  (umi_out_valid),
               .umi_out_cmd    (umi_out_cmd),
               .umi_out_dstaddr(umi_out_dstaddr),
               .umi_out_srcaddr(umi_out_srcaddr),
