   (// controls
    input             clk,
    input             nreset,
    input [1:0]       arbmode,
    input [7:0]       arbmask,
    // UMI input 0
    input             umi_in0_valid,
    input [CW-1:0]    umi_in0_cmd,
    input [AW-1:0]    umi_in0_dstaddr,
    input [AW-1:0]    umi_in0_srcaddr,
